Ludwig
download and install Ludwig which will allow you to download the library separately:

Download Address into your modules folder.
Use one of Ludwig's methods to download the library:
a) Run the ludwig:download Drupal Console command or the ludwig-download Drush command.

b) Go to /admin/reports/packages and download the library manually, then place it under address/lib as specified.
   Clear all caches.

Enable Address.
Note that when using Ludwig, updating the module will require re-downloading the library. Composer is recommended whenever possible.

